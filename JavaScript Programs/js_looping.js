var i=0;
var n=10;
document.write("Using while loop - Entry loop <br/>")
while(i<=n){
    document.write(i+"<br/>")
    i++;
}

var ii=0;
var nn=10;
document.write("<br/>Using do while loop - Exit loop<br/>")
do {
    document.write(ii+"<br/>");
    ii++;
}while(ii<=nn);

document.write("<br/>for loop<br/>")
for(j=0;j<=10;j++){
    document.write(j+"<br/>")
}